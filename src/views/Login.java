    private void lbl_usersOpenSettings(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbl_usersOpenSettings
		// TODO add your handling code here:
		FileDialog dialog = new FileDialog(this, "Seleccione un archivo", FileDialog.LOAD);
		dialog.setBounds(WIDTH / 2, HEIGHT / 2, 800, 800);
		dialog.setVisible(true);
		dialog.setLocationRelativeTo(null);
		if (dialog.getFile() != null) {
			String directory = dialog.getDirectory();
			String filename = dialog.getFile();
			String route = directory + filename;
			JsonReading.getInstance().jsonUsers(route);
		} else {
			System.out.println("No Seleccion√≥ Archivo");
		}
		JsonBlock.getInstance().createBlock();
    }//GEN-LAST:event_lbl_usersOpenSettings

